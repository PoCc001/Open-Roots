inline float osqrt_nan() {
	float_ul nan;
	nan.ul = FLOAT_NAN;
	return nan.f;
}

float osqrt_subnormal(float_ul* val) {
	if (val->f < 0.0f) {
		return osqrt_nan();
	}
	else if (val->f == 0.0f) {
		return val->f;
	}

	val->f *= (float)(1UL << 24);
	uint32_t i = (uint32_t)((val->ul >> 11) & ((1UL << 12) - 1UL));
	float v1 = OSQRT_NORM_MANT[i];
	float v2 = OSQRT_NORM_MANT[i + 1];
	float_ul d;
	d.f = v2 - v1;
	d.ul -= (11UL << 23);
	d.f = v1 + d.f * (float)(val->ul & ((1UL << 11) - 1UL));
	d.ul -= (8UL << 23);
	return d.f * OSQRT_NORM_EXP[val->ul >> 23];
}

float osqrt_normal(float_ul* val, uint32_t exp) {
	uint32_t i = (uint32_t)((val->ul >> 11) & ((1UL << 12) - 1UL));
	float v1 = OSQRT_NORM_MANT[i];
	float v2 = OSQRT_NORM_MANT[i + 1];
	float_ul d;
	d.f = v2 - v1;
	d.ul -= (11 << 23);
	d.f = v1 + d.f * (float)(val->ul & ((1UL << 11) - 1UL));
	return d.f * OSQRT_NORM_EXP[exp];
}

float osqrtf(const float x) {
	if (x < 0.0f) {
		return osqrt_nan();
	}

	float_ul val;
	val.f = x;
	uint32_t exp = val.ul >> 23;
	return exp == 255 ? x : (x < 1.175494e-38f ? osqrt_subnormal(&val) : osqrt_normal(&val, exp));
}

inline float orsqrtf(const float x) {
	return 1.0 / osqrtf(x);
}

